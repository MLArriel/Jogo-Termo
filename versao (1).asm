    push r1
    push r2
    push r3
    push r4
        
        loadn r0, #FlagIguais
        loadn r1, #0 ; Verifica se a flag está ligada
        loadn r3, #0 ; contador
        loadn r4, #5 ; critério de parada
        
        LoopGanha:
            loadi r2, r0
            
            cmp r2, r1
            jeq RtsGanha
            
            inc r0
            inc r3
            
            cmp r3, r4
            jne LoopGanha
            
            call Ganhou
    
    RtsGanha:
        pop r4    
        pop r3
        pop r2
        pop r1
        pop r0
        rts
        
